struct TreeNode {
  int val;
  TreeNode *left;
  TreeNode *right;
  TreeNode() : val(0), left(nullptr), right(nullptr) {}
  TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
  TreeNode(int x, TreeNode *left, TreeNode *right)
      : val(x), left(left), right(right) {}
};
// @lc code=start

class Solution {
 public:
  //对每个节点维护一个合法范围 (min, max)
  bool isValidBST(TreeNode *root) { return isValid(root, LONG_MIN, LONG_MAX); }
  bool isValid(TreeNode *node, long lower, long upper) {
    if (!node) return true;
    if (node->val <= lower || node->val >= upper) return false;

    return isValid(node->left, lower, node->val) &&
           isValid(node->right, node->val, upper);
  }
};
