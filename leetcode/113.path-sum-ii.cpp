struct TreeNode {
  int val;
  TreeNode *left;
  TreeNode *right;
  TreeNode() : val(0), left(nullptr), right(nullptr) {}
  TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
  TreeNode(int x, TreeNode *left, TreeNode *right)
      : val(x), left(left), right(right) {}
};
// @lc code=start

class Solution {
 public:
  vector<vector<int>> pathSum(TreeNode *root, int targetSum) {
    vector<vector<int>> res;
    vector<int> tmp;
    getPath(root, targetSum, res, tmp);
    return res;
  }

  void getPath(TreeNode *root, int targetSum, vector<vector<int>> &res,
               vector<int> &tmp) {
    if (!root) return;
    if (targetSum - root->val == 0 && !root->left && !root->right) {
      tmp.push_back(root->val);
      res.push_back(tmp);
      tmp.pop_back();
      return;
    }
    tmp.push_back(root->val);
    if (root->left) getPath(root->left, targetSum - root->val, res, tmp);
    if (root->right) getPath(root->right, targetSum - root->val, res, tmp);
    tmp.pop_back();
  }
};
