struct ListNode {
  int val;
  ListNode* next;
  ListNode() : val(0), next(nullptr) {}
  ListNode(int x) : val(x), next(nullptr) {}
  ListNode(int x, ListNode* next) : val(x), next(next) {}
};

// @lc code=start

class Solution {
 public:
  ListNode* reverseKGroup(ListNode* head, int k) {
    ListNode *group_k[k], *p = head, *res = nullptr, *pre = nullptr;
    while (p) {
      int i;
      for (i = 0; i < k && p; ++i) {
        group_k[i] = p;
        p = p->next;
      }
      if (i < k) break;
      if (pre) pre->next = group_k[k - 1];
      pre = group_k[0];
      group_k[0]->next = group_k[k - 1]->next;
      for (int id = k - 1; id > 0; --id) {
        group_k[id]->next = group_k[id - 1];
      }
      if (!res) res = group_k[k - 1];
    }
    return res ? res : head;
  }
};
